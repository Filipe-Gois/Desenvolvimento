//switch case

let num1 = 5;

switch (num1) {
  case 1:
    console.log("1");
    break;
  case 2:
    console.log("2");
    break;
  case 3:
    console.log("3");
    break;

  //simplificado
  case 4:
  case 5:
  case 6:
    console.log("premio de participação");
    break;

  default:
    console.log("default");
    break;
}
